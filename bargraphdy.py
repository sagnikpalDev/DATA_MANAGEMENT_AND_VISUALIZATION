import matplotlib.pyplot as plt


n = int(input("Enter number of bars: "))

labels = []
values = []


for i in range(n):
    label = input(f"Enter label for bar {i+1}: ")
    value = float(input(f"Enter value for {label}: "))

    labels.append(label)
    values.append(value)


plt.figure()
plt.bar(labels, values)


plt.xlabel("Categories")
plt.ylabel("Values")
plt.title("Bar Graph (User Input)")
plt.grid(axis='both')

plt.show()